package ufrrj.bruno.ia.graficos;

import java.awt.Color;

public class Fatia {
    double valor;
    Color cor;
    public Fatia(double valor,Color cor){
        this.valor = valor;
        this.cor = cor;
    }

    public double getValor() {
        return valor;
    }

    public Color getCor() {
        return cor;
    }
    
    
}
